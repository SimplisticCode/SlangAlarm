package playground.RuntimeUtils

import org.sireum._

object Utils {
  def Abs(x: R): R = {
    return Abs(x)
  }

  def Floor(x: R): Z = {
    return Floor(x)
  }

  def Div(x : Z, y : Z):Z={
    return Div(x, y)
  }

  def Rem(x : Z, y : Z):Z={
    return Rem(x, y)
  }

  def Mod(x : Z, y : Z):Z={
    return Mod(x, y)
  }

}
